def bins_to_secs(bins, sr=22050, hop=192):
    return bins * hop // sr

def secs_to_bins(secs, sr=22050, hop=192):
    # secs + 1 bc offset of -1 coming from somewhere
    return int(np.ceil((secs + 1) * sr / hop))
